def generate_from_code(
    filepath: str,
    language: str,
    category: str,
) -> str:
    """Generate a draft entry by analyzing a local code file."""
    ld = LANGUAGE_DEFAULTS.get(language, LANGUAGE_DEFAULTS["python"])
    path = Path(filepath)

    if not path.exists():
        print(f"Error: File not found: {filepath}", file=sys.stderr)
        sys.exit(1)

    source = path.read_text(encoding="utf-8", errors="replace")
    slug = path.stem.lower()
    title = f"{path.stem.replace('_', ' ').replace('-', ' ').title()} Pattern"

    # Extract function and class names
    funcs = re.findall(r"^(?:async\s+)?def\s+(\w+)", source, re.MULTILINE)
    classes = re.findall(r"^class\s+(\w+)", source, re.MULTILINE)
    imports = re.findall(r"^(?:from\s+\S+\s+)?import\s+(.+)", source, re.MULTILINE)

    tags = [slug, language, category]
    for f in funcs[:3]:
        tags.append(f.lower())
    for c in classes[:2]:
        tags.append(c.lower())

    when_to_use = "## When to Use\n"
    if funcs:
        when_to_use += f"- When you need to implement `{', '.join(funcs[:3])}` functionality\n"
    if classes:
        when_to_use += f"- When working with `{', '.join(classes[:3])}` class patterns\n"
    if not funcs and not classes:
        when_to_use += "- [TODO: Describe when to use this pattern]\n"

    standard_pattern = source[:3000]  # First 3000 chars
    if len(source) > 3000:
        standard_pattern += "\n\n# ... (truncated for draft)"

    return ENTRY_TEMPLATE.format(
        entry_id=f"{language}-{category}-{slug}",
        title=title,
        language=language,
        category=category,
        tags=", ".join(f'"{t}"' for t in tags[:8]),
        version=ld["version"],
        retrieval_hint=f"{slug} {language} {category} {' '.join(funcs[:3])} {' '.join(classes[:2])}",
        today=datetime.now().strftime("%Y-%m-%d"),
        language_code=ld["code_tag"],
        when_to_use=when_to_use,
        standard_pattern=standard_pattern,
        wrong_desc="TODO: Describe a common mistake",
        wrong_code="# TODO: Add bad code example",
        correct_desc="The right way",
        correct_code="# TODO: Add good code example",
        related=ld["related"],
    )


