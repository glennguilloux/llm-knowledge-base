def generate_from_prompt(
    prompt: str,
    language: str,
    category: str | None = None,
    offline: bool = False,
    llm_url: str | None = None,
    llm_model: str | None = None,
) -> str:
    """Generate a draft entry from a prompt. Uses LLM if available."""
    ld = LANGUAGE_DEFAULTS.get(language, LANGUAGE_DEFAULTS["python"])

    if not category:
        category = "stdlib"

    # Create a URL-safe slug from the prompt
    slug = re.sub(r"[^a-z0-9]+", "-", prompt.lower().strip())[:50].strip("-")
    title = prompt.strip()[:100]

    if offline or not llm_url:
        # Produce skeleton
        return SKELETON_TEMPLATE.format(
            entry_id=f"{language}-{category}-{slug}",
            title=title,
            language=language,
            category=category,
            tags=f'"{slug}", "{language}", "{category}"',
            version=ld["version"],
            retrieval_hint=f"{slug} {language} {category} how to use",
            today=datetime.now().strftime("%Y-%m-%d"),
            language_code=ld["code_tag"],
            related=ld["related"],
        )

    # Try LLM-assisted generation
    try:
        llm_prompt = f"""Generate a knowledge base entry for an LLM code reference system.

Topic: {prompt}
Language: {language}
Category: {category}

The entry should be a Markdown file with YAML frontmatter following this exact schema:

---
id: "{language}-{category}-{slug}"
title: "{title}"
language: "{language}"
category: "{category}"
tags: ["tag1", "tag2", "tag3"]
version: "{ld['version']}"
retrieval_hint: "keywords for search"
last_verified: "{datetime.now().strftime('%Y-%m-%d')}"
confidence: "draft"
---

# Title

## When to Use
- Concrete scenario 1
- Concrete scenario 2

## Standard Pattern
(complete runnable code with imports, type hints, error handling)

## Common Mistakes
# WRONG: description
(bad code)
# CORRECT: description
(good code)
(Include at least 3 WRONG/CORRECT pairs)

## Gotchas
- Pitfall 1
- Pitfall 2
- Pitfall 3

## Related
- related-entry.md

Include at the end:
> **⚠️ DRAFT ENTRY — Needs Human Review**

Return ONLY the markdown content, nothing else."""

        ctx = ssl.create_default_context()
        data = json.dumps({
            "model": llm_model or "local-model",
            "messages": [
                {"role": "system", "content": "You are a technical writer creating code pattern reference entries."},
                {"role": "user", "content": llm_prompt},
            ],
            "temperature": 0.3,
            "max_tokens": 4096,
        }).encode()

        req = urllib.request.Request(
            llm_url,
            data=data,
            headers={"Content-Type": "application/json"},
        )
        with urllib.request.urlopen(req, timeout=120, context=ctx) as resp:
            result = json.loads(resp.read().decode())
            content = result["choices"][0]["message"]["content"]

        # Ensure frontmatter is present
        if not content.startswith("---"):
            content = f"---\nid: \"{language}-{category}-{slug}\"\ntitle: \"{title}\"\nlanguage: \"{language}\"\ncategory: \"{category}\"\ntags: [\"{slug}\", \"{language}\", \"{category}\"]\nversion: \"{ld['version']}\"\nretrieval_hint: \"{slug} {language}\"\nlast_verified: \"{datetime.now().strftime('%Y-%m-%d')}\"\nconfidence: \"draft\"\n---\n\n{content}"

        return content

    except Exception as e:
        print(f"LLM generation failed ({e}), falling back to skeleton.", file=sys.stderr)
        return SKELETON_TEMPLATE.format(
            entry_id=f"{language}-{category}-{slug}",
            title=title,
            language=language,
            category=category,
            tags=f'"{slug}", "{language}", "{category}"',
            version=ld["version"],
            retrieval_hint=f"{slug} {language} {category} how to use",
            today=datetime.now().strftime("%Y-%m-%d"),
            language_code=ld["code_tag"],
            related=ld["related"],
        )


