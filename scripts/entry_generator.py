def _fetch_github_readme(repo: str, section: str | None = None, timeout: int = 15) -> str | None:
    """Fetch a GitHub repository's README via raw.githubusercontent.com."""
    try:
        ctx = ssl.create_default_context()
        # Try main branch first, then master
        for branch in ("main", "master"):
            url = f"https://raw.githubusercontent.com/{repo}/{branch}/README.md"
            try:
                req = urllib.request.Request(url, headers={"User-Agent": "llm-kb-generator/1.0"})
                with urllib.request.urlopen(req, timeout=timeout, context=ctx) as resp:
                    content = resp.read().decode("utf-8", errors="replace")
                    if section:
                        # Try to find the section
                        section_match = re.search(
                            rf"#+\s+{re.escape(section)}.*?\n(.*?)(?=\n#+\s|\Z)",
                            content, re.DOTALL | re.IGNORECASE
                        )
                        if section_match:
                            return section_match.group(1).strip()
                    return content
            except Exception:
                continue
        return None
    except Exception:
        return None


def generate_from_github(
    repo: str,
    section: str | None,
    language: str,
    category: str | None = None,
    offline: bool = False,
) -> str:
    """Generate a draft entry from a GitHub repository README."""
    ld = LANGUAGE_DEFAULTS.get(language, LANGUAGE_DEFAULTS["python"])

    # Derive title from repo name
    repo_name = repo.split("/")[-1]
    title = f"{repo_name.title()} - {section or 'Overview'}"

    if not category:
        category = "stdlib"

    slug = repo_name.lower()

    if offline:
        return SKELETON_TEMPLATE.format(
            entry_id=f"{language}-{category}-{slug}",
            title=title,
            language=language,
            category=category,
            tags=f'"{slug}", "{language}", "{category}", "{repo_name}"',
            version=ld["version"],
            retrieval_hint=f"{repo_name} {slug} {language} {section or ''} how to use",
            today=datetime.now().strftime("%Y-%m-%d"),
            language_code=ld["code_tag"],
            related=ld["related"],
        )

    readme_content = _fetch_github_readme(repo, section)

    if readme_content:
        # Extract code blocks
        code_blocks = re.findall(r"```.*?\n(.*?)```", readme_content, re.DOTALL)
        standard_pattern = "\n".join(code_blocks[:3]) if code_blocks else "# TODO: Add from README"

        # Extract paragraphs for "When to Use"
        paragraphs = [p.strip() for p in readme_content.split("\n") if len(p.strip()) > 50]
        when_to_use = "## When to Use\n"
        for p in paragraphs[:5]:
            if p and not p.startswith("#") and not p.startswith("```") and not p.startswith("!"):
                when_to_use += f"- {p[:200]}\n"
    else:
        standard_pattern = "# TODO: Add from GitHub README"
        when_to_use = "## When to Use\n- [TODO: Extract from README]\n"

    return ENTRY_TEMPLATE.format(
        entry_id=f"{language}-{category}-{slug}",
        title=title,
        language=language,
        category=category,
        tags=f'"{slug}", "{language}", "{category}", "{repo_name}"',
        version=ld["version"],
        retrieval_hint=f"{repo_name} {slug} {language} {section or ''} how to use",
        today=datetime.now().strftime("%Y-%m-%d"),
        language_code=ld["code_tag"],
        when_to_use=when_to_use,
        standard_pattern=standard_pattern[:3000],  # Truncate long READMEs
        wrong_desc="TODO: Describe a common mistake",
        wrong_code="# TODO: Add bad code example",
        correct_desc="The right way",
        correct_code="# TODO: Add good code example",
        related=ld["related"],
    )


