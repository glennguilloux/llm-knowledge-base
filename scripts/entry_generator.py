def determine_output_path(
    language: str,
    category: str,
    slug: str,
    kb_path: Path,
) -> Path:
    """Determine the output path for a generated entry."""
    # Map languages to their standard directory names
    lang_dirs = {
        "python": "python",
        "java": "java",
        "typescript": "typescript",
        "go": "go",
        "rust": "rust",
        "csharp": "csharp",
        "bash": "bash",
        "sql": "db",
        "yaml": "devops",
    }

    lang_dir = lang_dirs.get(language, language)
    base = kb_path / lang_dir / category
    base.mkdir(parents=True, exist_ok=True)

    filename = f"{slug}.md"
    return base / filename


def save_entry(content: str, output_path: Path, dry_run: bool = False) -> Path:
    """Save the generated entry to disk."""
    if dry_run:
        print(f"[DRY RUN] Would save to: {output_path}")
        return output_path

    output_path.write_text(content, encoding="utf-8")
    print(f"✅ Saved draft entry to: {output_path}")
    return output_path


