class TextExtractor(html.parser.HTMLParser):
    """Extract readable text from HTML, preserving code blocks."""

    def __init__(self):
        super().__init__()
        self.text_parts: list[str] = []
        self.code_parts: list[str] = []
        self.in_code = False
        self.in_pre = False
        self.in_script = False
        self.in_style = False

    def handle_starttag(self, tag, attrs):
        if tag in ("script", "style"):
            self.in_script = True
        elif tag == "pre":
            self.in_pre = True
        elif tag == "code" and not self.in_pre:
            self.in_code = True

    def handle_endtag(self, tag):
        if tag in ("script", "style"):
            self.in_script = False
        elif tag == "pre":
            self.in_pre = False
        elif tag == "code":
            self.in_code = False

    def handle_data(self, data):
        if self.in_script or self.in_style:
            return
        stripped = data.strip()
        if not stripped:
            return
        if self.in_pre or self.in_code:
            self.code_parts.append(stripped)
        else:
            self.text_parts.append(stripped)


def fetch_url(url: str, timeout: int = 15) -> str | None:
    """Fetch a URL and return readable text."""
    try:
        ctx = ssl.create_default_context()
        req = urllib.request.Request(url, headers={"User-Agent": "llm-kb-generator/1.0"})
        with urllib.request.urlopen(req, timeout=timeout, context=ctx) as resp:
            content_type = resp.headers.get("Content-Type", "")
            data = resp.read()

            if "text/html" in content_type:
                extractor = TextExtractor()
                extractor.feed(data.decode("utf-8", errors="replace"))
                # Combine text and code
                result = "\n".join(extractor.text_parts[:100])  # Limit text
                if extractor.code_parts:
                    result += "\n\n## Code Snippets\n```\n"
                    result += "\n".join(extractor.code_parts[:20])
                    result += "\n```"
                return result
            else:
                return data.decode("utf-8", errors="replace")
    except Exception as e:
        return f"# Error fetching URL\n\nCould not fetch {url}: {e}\n\nPlease add content manually."


def generate_from_url(
    url: str,
    language: str,
    category: str,
    offline: bool = False,
) -> str:
    """Generate a draft entry from a documentation URL."""
    ld = LANGUAGE_DEFAULTS.get(language, LANGUAGE_DEFAULTS["python"])

    # Derive title and slug from URL
    url_parts = url.rstrip("/").split("/")
    slug = url_parts[-1] if url_parts else "topic"
    title_words = slug.replace("-", " ").replace("_", " ").title()
    title = f"{title_words} in {language.title()}"

    if offline:
        content = SKELETON_TEMPLATE.format(
            entry_id=f"{language}-{category}-{slug}",
            title=title,
            language=language,
            category=category,
            tags=f'"{slug}", "{language}", "{category}"',
            version=ld["version"],
            retrieval_hint=f"{slug} {language} {category} how to use pattern",
            today=datetime.now().strftime("%Y-%m-%d"),
            language_code=ld["code_tag"],
            related=ld["related"],
        )
        return content

    # Fetch URL content
    fetched = fetch_url(url)

    # Extract code snippets from fetched content
    code_blocks = re.findall(r"```.*?\n(.*?)```", fetched or "", re.DOTALL)
    standard_pattern = "\n".join(code_blocks[:3]) if code_blocks else "# TODO: Add from documentation"

    # Try to extract first paragraphs for "When to Use"
    paragraphs = [p.strip() for p in (fetched or "").split("\n") if len(p.strip()) > 50]
    when_to_use = "## When to Use\n"
    for p in paragraphs[:5]:
        if p and not p.startswith("#") and not p.startswith("```"):
            when_to_use += f"- {p[:200]}\n"

    if not paragraphs:
        when_to_use += "- [TODO: Extract from documentation]\n"

    content = ENTRY_TEMPLATE.format(
        entry_id=f"{language}-{category}-{slug}",
        title=title,
        language=language,
        category=category,
        tags=f'"{slug}", "{language}", "{category}"',
        version=ld["version"],
        retrieval_hint=f"{slug} {language} {category} how to use pattern",
        today=datetime.now().strftime("%Y-%m-%d"),
        language_code=ld["code_tag"],
        when_to_use=when_to_use,
        standard_pattern=standard_pattern,
        wrong_desc="TODO: Describe a common mistake",
        wrong_code="# TODO: Add bad code example",
        correct_desc="The right way",
        correct_code="# TODO: Add good code example",
        related=ld["related"],
    )

    return content


