"""
Bootstrap new knowledge entries from external sources.

Sources:
  1. Documentation URLs (e.g., FastAPI docs, SQLAlchemy docs)
  2. GitHub README files
  3. Existing code files (extract patterns from real code)
  4. From a prompt (uses an LLM if available, otherwise produces a skeleton)

Usage:
  # From a documentation URL
  python scripts/entry_generator.py --from-url "https://fastapi.tiangolo.com/tutorial/security/oauth2-jwt/" --language python --category web

  # From a GitHub README
  python scripts/entry_generator.py --from-github "encode/httpx" --section "quickstart" --language python

  # From a local code file
  python scripts/entry_generator.py --from-code ./my_auth.py --language python --category web

  # From a prompt (uses an LLM if available, otherwise skeleton)
  python scripts/entry_generator.py --from-prompt "Redis pub/sub patterns in Python" --language python --category concurrency

  # Offline mode — always produce skeleton only
  python scripts/entry_generator.py --offline --from-prompt "Any topic" --language python

Output:
  - Generates a draft entry file following schema.md
  - Sets confidence: "draft" (needs human review)
  - Includes all required sections (some may be placeholder)
  - Saves to appropriate directory based on language/category
"""

